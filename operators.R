## Operators in R ##

# Arithmetic operators
x <- 12
y <- 10

x + y
x - y
x * y
x / y
x ^ 2
x %% 2
x %/% y

# Assignment operators
new_num <- 5
new_num <<- 10
11 -> new_num
12 ->> new_num

# Comparison operators
x <- 10
y <- 12

x > y
x < y
x >= y
x <= y
x == y
x != y

# Logical operators
TRUE & FALSE
TRUE && TRUE
TRUE | FALSE
TRUE == !FALSE
